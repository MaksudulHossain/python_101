beautiful_places = ["Santorini", "Kyoto", "Bali", "Venice", "Aspen"]

print("Original")
print(beautiful_places)
print("sorted wo modifying")
print(sorted(beautiful_places))
print("original now")
print(beautiful_places)
print("In reverse sorted")
beautiful_places.reverse()
print(beautiful_places)
beautiful_places.reverse()
print(beautiful_places)
print("Using sort()")
beautiful_places.sort()
print(beautiful_places)
beautiful_places.sort(reverse=True)
print(beautiful_places)


